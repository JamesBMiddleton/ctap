typedef struct {
    u32 placeholder;
} gfx_InitArg;
typedef enum { gfx_InitRet_OK, gfx_InitRet_MAP_INVALID } gfx_InitRet;
static gfx_InitRet gfx_Init(gfx_InitArg arg);

////////////////////////////////////////////////////////////////////////////////
///////////////////////////// INTERNAL IMPL ////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////


////////////////////////////////////////////////////////////////////////////////
////////////////////////////// API IMPL ////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

struct gfx__State {
    u32 placeholder;
    // pool_t
} static gfx__state = {0};

/*
 * Initialise the audio module. 
 *
 * @param args - initialisation arguments.
*/
static gfx_InitRet gfx_Init(gfx_InitArg args)
{
    gfx__state.placeholder = 1;
    return (args.placeholder) ? gfx_InitRet_OK : gfx_InitRet_MAP_INVALID;
}

////////////////////////////////////////////////////////////////////////////////
/////////////////////////////// UTEST IMPL /////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
#ifdef UTEST_GFX

static void utest_gfx_Untested(void)
{
    gfx_Init((gfx_InitArg){0});
}

#ifdef UTEST
static void utest_gfx_Main(void)
{
    util_SetLogCallback(utest_util_PrintfLoggerCallback);
    util_SetPanicCallback(utest_util_DoNothing);
    utest_gfx_Untested();
}

i32 main(void)
{
    utest_gfx_Main();
    return 0;
}
#endif // UTEST
#endif // UTEST_GFX
#endif // GFX_C
