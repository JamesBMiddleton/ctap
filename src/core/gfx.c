typedef struct {
    u32 placeholder;
} gfx_InitArg;
typedef enum { gfx_InitRet_OK, gfx_InitRet_MAP_INVALID } gfx_InitRet;
static gfx_InitRet gfx_Init(gfx_InitArg arg);

