typedef struct {
    uint placeholder;
} input_InitA;
typedef enum { input_InitR_OK, input_InitR_MAP_INVALID } input_InitR;
static input_InitR input_Init(input_InitA arg);

////////////////////////////////////////////////////////////////////////////////
///////////////////////////// INTERNAL IMPL ////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////


////////////////////////////////////////////////////////////////////////////////
////////////////////////////// API IMPL ////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

struct StateInput {
    uint placeholder;
    // pool_t
} static gStateInput = {0};

/*
 * Initialise the audio module. 
 *
 * @param args - initialisation arguments.
*/
static input_InitR input_Init(input_InitA args)
{
    gStateInput.placeholder = 1;
    return (args.placeholder) ? input_InitR_OK : input_InitR_MAP_INVALID;
}

////////////////////////////////////////////////////////////////////////////////
/////////////////////////////// UTEST IMPL /////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
#ifdef UTEST_INPUT

static void utest_input_Untested(void)
{
    input_Init((input_InitA){0});
}

#ifdef UTEST
static void utest_input_Main(void)
{
    util_RegisterEventHandlerLog(utest_util_EventHandlerLogPrintf);
    util_RegisterEventHandlerPanic(utest_util_EventHandlerPanicDoNothing);
    utest_input_Untested();
}

int main(void)
{
    utest_input_Main();
    return 0;
}
#endif // UTEST
#endif // UTEST_INPUT
